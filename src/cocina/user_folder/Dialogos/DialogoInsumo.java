    private void consultar() {
                
        try{            
            Class.forName("com.mysql.jdbc.Driver");
            String cadena = "jdbc:mysql://localhost/pro_bd?user=root&password=qonmqa3p";
            Connection con = (Connection) DriverManager.getConnection(cadena);            
            PreparedStatement stmt = null;            
            
            ResultSet table;           
            String sql = "select * from insumos where "
                    + "id_insumo = " + value;                    
            stmt = con.prepareStatement(sql);            
            table = stmt.executeQuery();        
            while (table.next()){
                tf_nombre.setText(table.getString(2));
                tf_categoria.setText(table.getString(4));                
            }
            
        }catch(ClassNotFoundException e1){
            JOptionPane.showMessageDialog(null,e1);
        }catch(SQLException e2){
            JOptionPane.showMessageDialog(null,e2);
        }catch(Exception e3){
            JOptionPane.showMessageDialog(null,e3);
        }
    }

    private void grabar() {
        String nombre = tf_nombre.getText();
        String categoria = tf_categoria.getText();
                
        String sql = "insert into insumos(nombre,categoria) values("
                + "\"" + nombre + "\"" + ","                  
                + "\"" + categoria + "\");";
        
        System.out.println(sql);
        
        connect_and_catch(sql,"Insumo Agregado"); 
    }

    private void modificar() {
        String nombre = tf_nombre.getText();
        String categoria = tf_categoria.getText();
                
        String sql = "update insumos "
                + "set nombre = " + "\"" + nombre + "\"" + ","                  
                + "categoria = "+ "\"" + categoria + "\""
                + "where id_insumo = " + value + ";";
        
        System.out.println(sql);
        
        connect_and_catch(sql,"Insumo Modificado"); 
    }

    private void delete(int value) {
        String sql = "delete from insumos where id_insumo = " + value + ";";                        
        connect_and_catch(sql,"Insumo Eliminado"); 
    }
